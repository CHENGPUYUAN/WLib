namespace WLib.WinCtrls.Extension
{
    /// <summary>
    /// 窗体或控件自动调整大小的选项
    /// </summary>
    public enum EResize
    {
        /// <summary>
        /// 不调整大小
        /// </summary>
        None,
        /// <summary>
        /// 自动调整大小，包括宽度和高度
        /// </summary>
        Auto,
        /// <summary>
        /// 自动调整宽度
        /// </summary>
        AutoWidth,
        /// <summary>
        /// 自动调整高度
        /// </summary>
        AutoHeight,
    }
}
